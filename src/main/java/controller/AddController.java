    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.sendRedirect("add.jsp");
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        EmployeeDb employeeDb = new EmployeeDb();
        String name = request.getParameter("name");
        System.out.println("Name"+name);
        String surname = request.getParameter("surname");
        String salary = request.getParameter("salary");
        String hireday = request.getParameter("hireday");
        String birthday = request.getParameter("birthday");
        String genderStr = request.getParameter("gender");
        Gender gender;
        try {
            gender = Gender.valueOfIgnoreCase(genderStr);
        } catch (IllegalArgumentException e) {
            gender = Gender.valueOf(genderStr);
        }
        Employee employee = new Employee(0,name,surname,Double.parseDouble(salary),LocalDate.parse(hireday,formatter),LocalDate.parse(birthday,formatter),gender);
        employeeDb.insert(employee);
        response.sendRedirect("home");

    }

